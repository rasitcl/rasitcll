package day03_WrapperClasses_Matematikselİşlemler;

public class C04_IncrementDecrement {

    public static void main(String[] args) {

        int sayı = 20 ;

        sayı = sayı + 5 ;

        System.out.println(sayı); // 25

        // sayıyı 3 artırıp yazdırın

        System.out.println( sayı + 3 ); // 28

        System.out.println(sayı); // 28

        // sayının değerini 2 katına çıkarıp yazdırın

        System.out.println( sayı *= 2); // 56

     }
}
