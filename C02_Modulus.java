package day03_WrapperClasses_Matematikselİşlemler;

public class C02_Modulus {

    public static void main(String[] args) {

        System.out.println( 85 % 6); // 1

        System.out.println( 281357 % 10); //birler basamağını yardıralım 7

        System.out.println( 283 % 2); // 283 tek mi çift mi ? 1 ==> Tek sayıdır

        System.out.println( 347 % 5); // 347 sayısı 5 ile tam bölünür mü ? 2 ==> tam bölünmez

        System.out.println( 85 / 6 ); // 14,.... ==> 14
        System.out.println( 23 / 5); // 4,6 ==> 4

        System.out.println( 283 / 10); // 28,3 ==> 28
        System.out.println( 28 / 10); // 2,8 ==> 2
        System.out.println( 2 / 10); // 8,2 ==> 0

    }
}
